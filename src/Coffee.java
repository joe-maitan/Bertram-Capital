public class Coffee {

    private String name;
    private double price;

    public Coffee(String _name, double _price) {
        this.name = _name;
        this.price = _price;
    } // End Coffee(_name, _price) constructor

    public String getName() {
        return this.name;
    } // End getName() method

    public double getPrice() {
        return this.price;
    } // End getPrice() method
    
} // End Coffee class
